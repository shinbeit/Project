package com.ticket.dto;

public class PerformDetailDTO {

	private int performDetailCode;
	private String performCode;
	private String performDetailImage;
	
	public int getPerformDetailCode() {
		return performDetailCode;
	}
	public void setPerformDetailCode(int performDetailCode) {
		this.performDetailCode = performDetailCode;
	}
	public String getPerformCode() {
		return performCode;
	}
	public void setPerformCode(String performCode) {
		this.performCode = performCode;
	}
	public String getPerformDetailImage() {
		return performDetailImage;
	}
	public void setPerformDetailImage(String performDetailImage) {
		this.performDetailImage = performDetailImage;
	}
	
}
